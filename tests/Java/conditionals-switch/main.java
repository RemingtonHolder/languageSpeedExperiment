class Main {
    public static void main(String[] args) {
        int x = 102114 / 2564;
        int y = 1018362 * 102;
        switch(x) {
            case 39:
                System.out.println(1024/256);
                System.out.println("X - T");
                System.out.println(x);
                break;
            default:
                System.out.println(1024*256);
                System.out.println("X - F");
                System.out.println(x);
                break;
        }
        switch(y) {
            case 9:
                System.out.println(1024/256);
                System.out.println("Y - T");
                System.out.println(y);
                break;
            default:
                System.out.println(1024*256);
                System.out.println("Y - F");
                System.out.println(y);
                break;
        }
    }
}