package main

import "fmt"

func main() {
	var x int = 102114 / 2564
	var y int = 1018362 * 102
	if x == 39 {
		fmt.Println(1024 / 256)
		fmt.Println("X - T")
		fmt.Println(x)
	} else {
		fmt.Println(1024 * 256)
		fmt.Println("X - F")
		fmt.Println(x)
	}
	if y == 9 {
		fmt.Println(1024 / 256)
		fmt.Println("Y - T")
		fmt.Println(y)
	} else {
		fmt.Println(1024 * 256)
		fmt.Println("Y - F")
		fmt.Println(y)
	}
}
